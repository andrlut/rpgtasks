/**
 * Shape of rows in our public.* tables (mirrors Supabase schema).
 * Hand-maintained for V0; replace with `supabase gen types typescript`
 * once the CLI is happily authed.
 */

export type DimensionId =
  | 'health'
  | 'strength'
  | 'mind'
  | 'wealth'
  | 'bonds'
  | 'craft';

/**
 * Sub-dimensions: 2 per dim. Tasks/skills can optionally point at one of
 * these for finer-grained organization (and per-sub subjective scoring).
 */
export type SubId =
  | 'sleep' | 'nutrition'
  | 'movement' | 'dexterity'
  | 'learn' | 'contemplate'
  | 'money' | 'career'
  | 'circle' | 'romance'
  | 'play' | 'build';

export type TaskType = 'one_shot' | 'daily' | 'weekly';

/**
 * Source of truth for when a task is "due". `task_type` is preserved as
 * a legacy hint but `recurrence` is what queries should consult.
 *
 * - one_shot: due once, ever. Disappears from "today" after first completion.
 * - daily: due every day. `target_count > 1` lets a single task be done
 *   multiple times per day (e.g. brush teeth 3x).
 * - weekly: due only on the listed weekdays (0=Sun, 6=Sat).
 * - monthly: due on the given day-of-month (skipped silently in months
 *   without that day, e.g. day=31 in February).
 */
export type Recurrence =
  | { type: 'one_shot' }
  | { type: 'daily' }
  | { type: 'weekly'; days: number[] }
  | { type: 'monthly'; day: number };

export type RecurrenceType = Recurrence['type'];

export interface Dimension {
  id: DimensionId;
  display_name: string;
  color: string;
  icon: string;
  sort_order: number;
}

export interface DimensionSub {
  id: SubId;
  dimension_id: DimensionId;
  display_name: string;
  icon: string;
  sort_order: number;
}

/**
 * Where a sub score came from. 'self' is the user's own gut rating,
 * 'questionnaire' is a derived score from the (future) onboarding quiz.
 */
export type AssessmentSource = 'self' | 'questionnaire';

export interface CharacterSubScore {
  character_id: string;
  source: AssessmentSource;
  sub_id: SubId;
  /** 0-5. */
  score: number;
  updated_at: string;
}

export interface AssessmentLogEntry {
  id: string;
  character_id: string;
  source: AssessmentSource;
  sub_id: SubId;
  score: number;
  recorded_at: string;
  /** Set when this row was generated by a questionnaire submission. NULL for
   * `source='self'` taps and any `source='questionnaire'` rows that predate
   * the session linkage. */
  session_id: string | null;
}

export interface QuestionnaireSession {
  id: string;
  character_id: string;
  taken_at: string;
  duration_seconds: number | null;
}

export interface QuestionnaireAnswerRow {
  id: string;
  session_id: string;
  question_id: string;
  raw_value: number;
}

export interface Profile {
  id: string;
  display_name: string;
  avatar_url: string | null;
  created_at: string;
  updated_at: string;
}

export interface Character {
  id: string;
  total_xp: number;
  coins: number;
  created_at: string;
  updated_at: string;
}

export interface CharacterDimension {
  character_id: string;
  dimension_id: DimensionId;
  xp: number;
}

export type MetricType = 'reps' | 'minutes' | 'pages' | 'km' | 'ml' | 'custom';

export interface Task {
  id: string;
  character_id: string;
  title: string;
  description: string | null;
  difficulty: 1 | 2 | 3 | 4 | 5;
  task_type: TaskType;
  recurrence: Recurrence;
  target_count: number;
  is_archived: boolean;
  created_at: string;
  updated_at: string;
  /**
   * Optional metric scaling — when set, the task's target value scales
   * with difficulty: base_value + increment_per_star * (difficulty - 1).
   * All four scaling fields are set together or all null.
   */
  metric_type: MetricType | null;
  metric_label: string | null;
  base_value: number | null;
  increment_per_star: number | null;
  /** NOT NULL after migration 0007 — every task lives under a sub. */
  sub_id: SubId;
}

export interface TaskTemplate {
  id: string;
  title: string;
  description: string | null;
  sub_id: SubId;
  difficulty: 1 | 2 | 3 | 4 | 5;
  task_type: TaskType;
  recurrence: Recurrence;
  target_count: number;
  metric_type: MetricType | null;
  metric_label: string | null;
  base_value: number | null;
  increment_per_star: number | null;
  sort_order: number;
}

/** Task with its linked dimensions (joined via task_dimension). */
export interface TaskWithDimensions extends Task {
  dimensions: DimensionId[];
}

export interface TaskCompletion {
  id: string;
  task_id: string;
  character_id: string;
  completed_at: string;
  xp_granted: number;
  coins_granted: number;
  /** Star difficulty actually used for this completion. */
  selected_difficulty: 1 | 2 | 3 | 4 | 5;
}

